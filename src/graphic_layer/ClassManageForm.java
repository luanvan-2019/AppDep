    private void tbClassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClassMouseClicked
        int row = tbClass.getSelectedRow();
        ClassModel selectedClass = classes.get(row);
        txtClassID.setText(selectedClass.getId());
        txtClassName.setText(selectedClass.getName());
        txtCourse.setText(selectedClass.getCourse());
        txtMember.setText(String.valueOf(selectedClass.getNumberOfStudent()));
        cbbMonitor.setSelectedItem(selectedClass.getMonitor());
        cbbTeacher.setSelectedItem(selectedClass.getTeacher());
        btnEditClass.setEnabled(true);
        btnDeleteClass.setEnabled(true);

        // Load list of students belong to selected class
        loadStudentInClass(txtClassID.getText());
    }//GEN-LAST:event_tbClassMouseClicked

    private void btnSearchClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchClassActionPerformed
        String key = txtSearchClass.getText();
        classes = classBS.searchClass(key);
        loadClassInfo();
        btnEditClass.setEnabled(false);
        btnDeleteClass.setEnabled(false);
        txtClassID.setText("");
        txtClassName.setText("");
        txtCourse.setText("");
        txtMember.setText("");
        cbbMonitor.setSelectedIndex(0);
        cbbTeacher.setSelectedIndex(0);
    }//GEN-LAST:event_btnSearchClassActionPerformed

    private void btnEditClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditClassActionPerformed
        ClassModel model = new ClassModel();
        model.setId(txtClassID.getText());
        model.setName(txtClassName.getText());
        model.setCourse(txtCourse.getText());
        int member;
        try {
            member = Integer.parseInt(txtMember.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Sỉ số phải là số nguyên dương", "Error", JOptionPane.ERROR_MESSAGE);
            txtMember.requestFocus();
            return;
        }
        if (member <= 0) {
            JOptionPane.showMessageDialog(this, "Sỉ số phải là số nguyên dương", "Error", JOptionPane.ERROR_MESSAGE);
            txtMember.requestFocus();
            return;
        }
        model.setNumberOfStudent(member);
        if (cbbMonitor.getSelectedIndex() > 0) {
            model.setMonitor(cbbMonitor.getSelectedItem().toString());
        }
        if (cbbTeacher.getSelectedIndex() > 0) {
            model.setTeacher(cbbTeacher.getSelectedItem().toString());
        }

        int row = classBS.update(model);
        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Update thành công!", "Info", JOptionPane.INFORMATION_MESSAGE);
            classes = classBS.getAll();
            loadClassInfo();
        } else {
            JOptionPane.showMessageDialog(this, "Update thất bại!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditClassActionPerformed

    private void btnDeleteClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteClassActionPerformed
        String classID = txtClassID.getText();
        int row = classBS.delete(classID);
        if (row > 0) {
            JOptionPane.showMessageDialog(this, "Delete thành công!", "Info", JOptionPane.INFORMATION_MESSAGE);
            resetClass();
        } else {
            JOptionPane.showMessageDialog(this, "Delete thất bại!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteClassActionPerformed

