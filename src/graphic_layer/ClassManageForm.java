    private void resetClass() {
        txtSearchClass.setText("");
        btnEditClass.setEnabled(false);
        btnDeleteClass.setEnabled(false);
        txtClassID.setText("");
        txtClassName.setText("");
        txtCourse.setText("");
        txtMember.setText("");
        cbbMonitor.setSelectedIndex(0);
        cbbTeacher.setSelectedIndex(0);
        classes = classBS.getAll();
        loadClassInfo();
        loadStudentInClass("");
    }

    // Load list of students belong to the specific class
    private void loadStudentInClass(String classID) {
        Vector<String> columns = new Vector<>();
        columns.add("Mã SV");
        columns.add("Họ Tên");

        Vector<Vector> datas = new Vector<>();
        List<StudentModel> students = studentBS.getStudentsInClass(classID);
        for (StudentModel student : students) {
            Vector rows = new Vector();
            rows.add(student.getId());
            rows.add(student.getName());
            datas.add(rows);
        }
        dtm = new DefaultTableModel(datas, columns);
        tbStudentInClass.setModel(dtm);

        // Set size for column in the table
        TableColumnModel columnModel = tbStudentInClass.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(30);
        columnModel.getColumn(1).setPreferredWidth(120);
    }

    // Load info of class
    private void loadClassInfo() {
        Vector<String> columns = new Vector<>();
        columns.add("Mã Lớp");
        columns.add("Tên Lớp");
        columns.add("Khóa Học");
        columns.add("Sĩ Số");
        columns.add("Lớp Trưởng");
        columns.add("GVCN");

        Vector<Vector> datas = new Vector<>();
        for (ClassModel model : classes) {
            Vector rows = new Vector();
            rows.add(model.getId());
            rows.add(model.getName());
            rows.add(model.getCourse());
            rows.add(model.getNumberOfStudent());
            if (!StringUtils.isBlank(model.getMonitor())) {
                StudentModel student = studentBS.getStudentByID(model.getMonitor());
                if (student != null) {
                    rows.add(student.getName());
                } else {
                    rows.add("");
                }
            } else {
                rows.add("");
            }
            if (!StringUtils.isBlank(model.getTeacher())) {
                TeacherModel teacher = teacherBS.getByID(model.getTeacher());
                if (teacher != null) {
                    rows.add(teacher.getName());
                } else {
                    rows.add("");
                }
            } else {
                rows.add("");
            }
            datas.add(rows);
        }
        dtm = new DefaultTableModel(datas, columns);
        tbClass.setModel(dtm);

        // Set size for column in the table
        TableColumnModel columnModel = tbClass.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(30);
        columnModel.getColumn(1).setPreferredWidth(100);
        columnModel.getColumn(2).setPreferredWidth(100);
        columnModel.getColumn(3).setPreferredWidth(30);
        columnModel.getColumn(4).setPreferredWidth(100);
        columnModel.getColumn(5).setPreferredWidth(100);

    }

    private void loadMonitorID() {
        List<String> ids = studentBS.getIDs();
        ids.forEach(item -> cbbMonitor.addItem(item));
    }

    private void loadTeacherID() {
        List<String> ids = teacherBS.getIDs();
        ids.forEach(item -> cbbTeacher.addItem(item));
    }

